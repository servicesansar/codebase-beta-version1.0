package com.servicesansar.model;

public class MainSubMenu {
	private Integer mainMenuid;
	private Integer subMenuid;
	private String mainServiceName;
	private String subServiceName;
	private Integer sequence;
	
	public Integer getMainMenuid() {
		return mainMenuid;
	}
	public void setMainMenuid(Integer mainMenuid) {
		this.mainMenuid = mainMenuid;
	}
	public Integer getSubMenuid() {
		return subMenuid;
	}
	public void setSubMenuid(Integer subMenuid) {
		this.subMenuid = subMenuid;
	}
	public String getMainServiceName() {
		return mainServiceName;
	}
	public void setMainServiceName(String mainServiceName) {
		this.mainServiceName = mainServiceName;
	}
	public String getSubServiceName() {
		return subServiceName;
	}
	public void setSubServiceName(String subServiceName) {
		this.subServiceName = subServiceName;
	}
	public Integer getSequence() {
		return sequence;
	}
	public void setSequence(Integer sequence) {
		this.sequence = sequence;
	}
	
	
	
}
